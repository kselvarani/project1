package com.selva.bean;

public class Campaign {
private String campaignname,campaignstartdate,campaignenddate,campaigncontactperson,eventname;

public String getCampaignname() {
	return campaignname;
}

public void setCampaignname(String campaignname) {
	this.campaignname = campaignname;
}

public String getCampaignstartdate() {
	return campaignstartdate;
}

public void setCampaignstartdate(String campaignstartdate) {
	this.campaignstartdate = campaignstartdate;
}

public String getCampaignenddate() {
	return campaignenddate;
}

public void setCampaignenddate(String campaignenddate) {
	this.campaignenddate = campaignenddate;
}

public String getCampaigncontactperson() {
	return campaigncontactperson;
}

public void setCampaigncontactperson(String campaigncontactperson) {
	this.campaigncontactperson = campaigncontactperson;
}

public String getEventname() {
	return eventname;
}

public void setEventname(String eventname) {
	this.eventname = eventname;
}


}
